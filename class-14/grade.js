var marks=parseFloat(prompt("Enter your Marks :"));

if(marks>= 80){
    console.log("A+");
}
else if(marks>= 70){
    console.log("A");
}
else if(marks>= 60){
    console.log("B");
}
else if(marks>= 50){
    console.log("C");
}
else if(marks>= 40){
    console.log("D");
}
else{
    console.log("Fail");
}